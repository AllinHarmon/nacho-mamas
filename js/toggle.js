// add event listeners to #starters show/hide button
	$('#starters').click(function(e){
		e.preventDefault();
		var link = this;
		
		// changes button status
		$('#starters .menu-type').toggleClass('hide-menu');
		$('#starters .menu-item').toggleClass('hide-menu');
		$('#starters .menu-subtitle').toggleClass('hide-menu');
		$('#starters .menu-description').toggleClass('hide-menu');
		
		
	});
	
